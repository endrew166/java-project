package io.github.jiangdequan;

public class Classe {
    private String nome;

    public Classe(String nome) {
        this.nome = nome;

    }

    /**
     * @return String return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

}